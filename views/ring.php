                            باشد.
                            نقشه استقرار خودروها در پاركينگ، به نحوي كه امكان مانور هر خودرو به صورت مستقل ميسر باشد،
                            بايستي
                            در هنگام درخواست صدور پروانه همراه ساير نقشه‏ ها ارائه شود.

